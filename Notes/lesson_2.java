//! Displaying Program Output!! 

// We use --> { System.out.print() } OR { System.out.println() } to print something

package Notes;

public class lesson_2 {
  public static void main(String[] args){
    //Print in the same line
    System.out.print("Hello World");
    System.out.print("I'm Hope");
    //* The output is Hello WorldI'm Hope

    System.out.println(" ");

    //Print in new line
    System.out.println("Hello World");
    System.out.println("I'm Hope");
    /*  The output is
          Hello World
          I'm Hope
     */

    //Print double quote
    System.out.println("He said that \"I'm from Palestine\" but I live in \"Japan\".");
    //* The output is He said that "I'm from Palestine" but I live in "Japan".

    //Print new line
    System.out.println("He said that \nI'm from Palestine \nbut I live in Japan.");
    /*  The output is
          He said that
          I'm from Palestine
          but I live in Japan.
     */

    //Print backslash
    System.out.println("This file located in C:\\Users\\Hope\\Documents\\Java"); 
    //*The output is This file located in C:\Users\Hope\Documents\Java  

    //Print tab
    System.out.println("I'm a Mechatronics Engineer \t & \t Full Stack Developer");
    //* The output is I'm a Mechatronics Engineer      &       Full Stack Developer
  }
  
}

/*_______________________________*/

//? To print newline use --> { \n }
//? To print tab use --> { \t }
//? To Carriage return (Position the screen curser at the beginning of the current line) use --> { \r }
//? To print backslash use --> { \\ }
//? To print double quote use --> { \" }

